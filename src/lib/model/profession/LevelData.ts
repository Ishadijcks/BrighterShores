export const getLevel = (exp: number): number => {
	return expPerLevel.findIndex((xp) => xp > exp) - 1;
};

export const getExp = (level: number): number => {
	return expPerLevel[level];
};

export const getExpThisLevel = (exp: number): number => {
	return exp - getExp(getLevel(exp));
};

export const getTotalExpForLevel = (level: number): number => {
	return getExp(level + 1) - getExp(level);
};

export const getExpLeftForLevel = (exp: number): number => {
	return getTotalExpForLevel(getLevel(exp)) - exp;
};

export const getLevelProgress = (exp: number): number => {
	const level = getLevel(exp);
	return getExpThisLevel(exp) / getTotalExpForLevel(level);
};
